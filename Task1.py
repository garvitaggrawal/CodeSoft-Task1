a=int(input("Enter 1st number "))
b=int(input("Enter 2nd number "))
c=input("Choose operration(+,-,*,/)")

if(c=='+'):
        print("Result=",a+b)
elif(c=='-'):
        print("Result=",a-b)
elif(c=='*'):
        print("Result=",a*b)
elif(c=='/'):
        print("Result=",a/b)
else:
    
        print("Wrong operation choose")
    
